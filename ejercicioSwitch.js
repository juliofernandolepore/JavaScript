//uso de la estructura switch

let diaSemana = 8;

switch (diaSemana) {
    case 1:
        console.log("domingo"); break
    case 2:
        console.log("lunes"); break
    case 3:
        console.log("martes"); break
    case 4:
        console.log("miercole"); break
    case 5:
        console.log("jueves"); break
    case 6:
        console.log("viernes"); break
    case 7:
        console.log("sabado"); break
    default:
        console.log(`el dia ${diaSemana} no es un dia de la semana valido`)

}