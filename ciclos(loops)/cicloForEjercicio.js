//practicando con ciclo for algunos incrementos y decrementos ( 3 en 3)

let maximoRepeticiones = 20, minimo= -20;

for (iterador = 0; iterador <= maximoRepeticiones; iterador += 3) {
    console.log(`la iteracion es la siguiente: ${iterador}`)
}

for(iterador = 0; iterador >= minimo; iterador -= 3) {
    console.log(`la iteracion es la siguiente: ${iterador}`)
}